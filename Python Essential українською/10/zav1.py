# Написати функцію, яка за допомогою регулярних виразів розбиває текст на окремі слова і знаходить частоту окремих слів.


import re
from collections import Counter

def word_frequency(text):
    # Використовуємо регулярний вираз для знаходження всіх слів у тексті
    words = re.findall(r'\b\w+\b', text.lower())
    # Підраховуємо частоту слів
    frequency = Counter(words)
    return frequency

# Приклад використання функції
text = "Це приклад тексту. Це ще один приклад!"
frequency = word_frequency(text)
print(frequency)


# Пояснення:

# re.findall(r'\b\w+\b', text.lower()): Цей рядок використовує регулярний вираз для знаходження всіх слів у тексті.
# \b\w+\b шукає будь-яку послідовність символів, яка складається з літер, цифр та символу підкреслення між межами слів (\b).
# text.lower(): Перетворює текст на нижній регістр, щоб забезпечити, що підрахунок не чутливий до регістру.
# Counter(words): Використовуємо Counter з модуля collections для підрахунку частоти кожного слова.
# Цей код розіб'є текст на слова, перетворить їх до нижнього регістру, а потім підрахує частоту кожного слова.
# Результатом буде словник, де ключами є слова, а значеннями — їх частота у тексті.