# Напишіть функцію, яка буде аналізувати текст, що надходить до неї, і виводити тільки унікальні слова на екран, загальну
# кількість слів і кількість унікальних слів.


def analyze_text(text):
    # Розбиваємо текст на слова, використовуючи регулярний вираз для видалення пунктуації
    import re
    words = re.findall(r'\b\w+\b', text.lower())

    total_words = len(words)  # загальна кількість слів

    # Створюємо множину для унікальних слів
    unique_words = set(words)
    unique_count = len(unique_words)  # кількість унікальних слів

    # Виводимо унікальні слова
    print("Унікальні слова:")
    for word in sorted(unique_words):
        print(word)

    # Виводимо загальну кількість слів і кількість унікальних слів
    print(f"\nЗагальна кількість слів: {total_words}")
    print(f"Кількість унікальних слів: {unique_count}")

# Приклад використання:
user_text = """
    This is a test. This is only a test! Please do not panic. 
    This is just to demonstrate the functionality of the program.
"""
analyze_text(user_text)

