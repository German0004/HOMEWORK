# Створіть магазин канцтоварів використовуючи списки для зберігання елементів. Для додавання елементів створіть функцію,
# яка буде запитувати дані в користувача і зібрані дані у вигляді кортежу додавати у створений список на початку.
# Результат вивести на екран. Під час створення дотримуйтесь правил специфікації PEP 8.


def add_item(items):
    """
    Функція для додавання елемента до списку канцтоварів.
    Запитує дані у користувача та додає їх у вигляді кортежу на початку списку.
    """
    name = input("Введіть назву товару: ")
    price = float(input("Введіть ціну товару: "))
    quantity = int(input("Введіть кількість товару: "))

    item = (name, price, quantity)
    items.insert(0, item)

def display_items(items):
    """
    Функція для відображення списку канцтоварів.
    """
    print("\nСписок канцтоварів:")
    for i, item in enumerate(items, start=1):
        name, price, quantity = item
        print(f"{i}. Назва: {name}, Ціна: {price}, Кількість: {quantity}")

def main():
    """
    Головна функція програми.
    """
    items = []

    while True:
        print("\nМагазин канцтоварів")
        print("1. Додати товар")
        print("2. Показати всі товари")
        print("0. Вихід")

        choice = input("Виберіть опцію: ")

        if choice == '1':
            add_item(items)
        elif choice == '2':
            display_items(items)
        elif choice == '0':
            print("Вихід з програми.")
            break
        else:
            print("Невірний вибір. Спробуйте ще раз.")

if __name__ == "__main__":
    main()


