# –ø—ñ–¥–Ω—è—Ç–∏—Å—è –Ω–∞ –∑–∞–¥–∞–Ω—É —Å—Ö–æ–¥–∏–Ω–∫—É.

# –¶–µ –∫–ª–∞—Å–∏—á–Ω–∞ –∑–∞–¥–∞—á–∞ –∑ —Ç–µ–æ—Ä—ñ—ó —á–∏—Å–µ–ª, —è–∫–∞ –æ–ø–∏—Å—É—î—Ç—å—Å—è –ø–æ—Å–ª—ñ–¥–æ–≤–Ω—ñ—Å—Ç—é –§—ñ–±–æ–Ω–∞—á—á—ñ. –©–æ–± –ø—ñ–¥–Ω—è—Ç–∏—Å—è –Ω–∞ –∑–∞–¥–∞–Ω—É —Å—Ö–æ–¥–∏–Ω–∫—É n, –∫—ñ–ª—å–∫—ñ—Å—Ç—å
# —Å–ø–æ—Å–æ–±—ñ–≤, —è–∫–∏–º–∏ —Ü–µ –º–æ–∂–Ω–∞ –∑—Ä–æ–±–∏—Ç–∏, –≤—ñ–¥–ø–æ–≤—ñ–¥–∞—î F(n+1), –¥–µ F(n) ‚Äî —Ü–µ n-–µ —á–∏—Å–ª–æ –§—ñ–±–æ–Ω–∞—á—á—ñ.
# –ü–æ—è—Å–Ω–µ–Ω–Ω—è:
# ‚Ä¢	–Ø–∫—â–æ –º–∏ –∑–Ω–∞—Ö–æ–¥–∏–º–æ—Å—è –Ω–∞ —Å—Ö–æ–¥–∏–Ω—Ü—ñ n, —Ç–æ –Ω–∞ –Ω–µ—ó –º–æ–∂–Ω–∞ –ø–æ—Ç—Ä–∞–ø–∏—Ç–∏ –∑ –ø–æ–ø–µ—Ä–µ–¥–Ω—å–æ—ó —Å—Ö–æ–¥–∏–Ω–∫–∏ (n‚àí1) –∞–±–æ –ø–µ—Ä–µ—Å—Ç—Ä–∏–±–Ω—É—Ç–∏
# —á–µ—Ä–µ–∑ –æ–¥–Ω—É (n-2).
# ‚Ä¢	–¢–∞–∫–∏–º —á–∏–Ω–æ–º, –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Å–ø–æ—Å–æ–±—ñ–≤ –ø—ñ–¥–Ω—è—Ç–∏—Å—è –Ω–∞ nnn-—É —Å—Ö–æ–¥–∏–Ω–∫—É –¥–æ—Ä—ñ–≤–Ω—é—î —Å—É–º—ñ –∫—ñ–ª—å–∫–æ—Å—Ç—ñ —Å–ø–æ—Å–æ–±—ñ–≤ –ø—ñ–¥–Ω—è—Ç–∏—Å—è –Ω–∞ n‚àí1 —ñ
# –Ω–∞ n‚àí2 —Å—Ö–æ–¥–∏–Ω–∫–∏.
# ‚Ä¢	–¶–µ —Å–∞–º–µ –≤–∏–∑–Ω–∞—á–µ–Ω–Ω—è —á–∏—Å–µ–ª –§—ñ–±–æ–Ω–∞—á—á—ñ: F(n)=F(n‚àí1)+F(n‚àí2).
# –§–æ—Ä–º–∞–ª—å–Ω–æ, –¥–ª—è –ø—ñ–¥–π–æ–º—É –Ω–∞ nnn-—É —Å—Ö–æ–¥–∏–Ω–∫—É, –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Å–ø–æ—Å–æ–±—ñ–≤ –≤–∏–∑–Ω–∞—á–∞—î—Ç—å—Å—è —è–∫ F(n+1)F(n+1)F(n+1), –¥–µ:
# ‚Ä¢	F(0)=0
# ‚Ä¢	F(1)=1
# ‚Ä¢	F(n)=F(n‚àí1)+F(n‚àí2) –¥–ª—è n‚â•2n


def fibonacci(n):
    if n == 0:
        return 0
    elif n == 1:
        return 1

    a, b = 0, 1
    for _ in range(2, n + 1):
        a, b = b, a + b
    return b


def ways_to_climb(n):
    return fibonacci(n + 1)


# –ü—Ä–∏–∫–ª–∞–¥–∏
