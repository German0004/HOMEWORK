# Напишіть рекурсивну функцію, яка обчислює суму натуральних чисел, які входять до заданого проміжку


def sum_natural_numbers(a, b):
    # Якщо a більше b, то немає чисел для додавання, повертаємо 0
    if a > b:
        return 0
    # Якщо a дорівнює b, то повертаємо a (або b)
    elif a == b:
        return a
    # Рекурсивно додаємо a і викликаємо функцію для наступного числа
    else:
        return a + sum_natural_numbers(a + 1, b)


# Приклад використання функції
a = 1
b = 5
print(sum_natural_numbers(a, b))  # Виведе: 15 (1 + 2 + 3 + 4 + 5)


# Цей код працює наступним чином:
#
# Якщо початкове значення a більше за кінцеве значення b, то немає чисел для додавання, і функція повертає 0.
# Якщо a дорівнює b, то повертається значення a (або b), оскільки це єдине число у проміжку.
# Інакше функція додає поточне значення a до результату рекурсивного виклику функції зі значенням a + 1 і тим же b.
